# Operasi Aritmatika
a = 10
b = 5 
# Operasi Tambah +
hasil = a + b
print(a, '+', b, '=' , hasil)

# Pengurangan -
hasil = a - b
print(a, '-', b, '=' , hasil)

# Perkalian *
hasil = a * b
print(a, '*', b, '=' , hasil)

# Pembagian / 
hasil = a / b
print(a, '/', b, '=' , hasil)

# Operasi Eksponen (Pangkat) ** 
hasil = a ** b
print(a, 'Pangkat', b, '=' , hasil)

# Operasi Modulus %
hasil = a % b
print(a, '%', b, '=' , hasil)

# Operasi floor division  //
hasil = a // b
print(a, '//', b, '=' , hasil)

# Prioritas operasi, Operational precedence
# 1. ()
# 2. exponen **
# 3. perkalian dan teman teman * / ** % //
# 4. pertambahan dan pengurangan + -

x = 5
y = 4
z = 10
hasil = y * (z + x) 
print(y, '*', z,'+', x, '=',hasil )
